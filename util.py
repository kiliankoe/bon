from time import sleep

def cat_announcement(p):
    """
    Cats get very irritated by spontaneous prints, so we try to announce
    the print before we do it.
    """
    p.ln(1)
    sleep(1)
    p.ln(1)
    sleep(0.5)
    p.ln(1)
    sleep(0.5)
    p.ln(1)
    sleep(0.5)
    p.ln(1)
    sleep(0.1)
